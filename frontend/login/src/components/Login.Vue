<script>
import axios from "axios";
import useVuelidate from "@vuelidate/core";
import { required, email } from "@vuelidate/validators";

export default {
  data() {
    return {
      v$: useVuelidate(),
      username: "",
      password: "",
      at: "",
      validation: null,
    };
  },
  validations() {
    return {
      username: { required, email }, // Matches this.firstName
      password: { required }, // Matches this.lastName
    };
  },
  methods: {
    async onSubmit() {
      // POST request using axios with async/await
      const information = {
        username: this.username,
        password: this.password,
      };
      const response = await axios.post(
        "http://127.0.0.1:4000/token",
        information
      );
      console.log(response);
      console.log(response.data.access_token);
      localStorage.setItem("access_token", response.data.access_token);
      if (localStorage.access_token) {
        this.at = localStorage.access_token;
      }

      this.username = "";
      this.password = "";
    },
    async onSubmit1() {
      const result = await this.v$.$validate();
      if (result) {
        // alert("success");
        this.validation = true;
        this.onSubmit();
      } else {
        // alert("error");
        this.validation = false;
      }
    },
    logOut() {
      localStorage.removeItem("access_token");
      this.at = "";
    },
  },
  mounted() {
    let access_token_1 = "";
    if (localStorage.access_token) {
      access_token_1 = localStorage.access_token;
      console.log("first run: " + access_token_1);
      this.at = access_token_1;
    }
  },
};
</script>

<template>
  <div v-if="at != ''" class="noti">
    You are logged in
    <button @click="logOut">logout</button>
  </div>
  <div v-else>
    <div>
      <div class="container__login"></div>
      <div class="background">
        <div class="shape"></div>
        <div class="shape"></div>
      </div>
      <form @submit.prevent="onSubmit">
        <h3>Login Here</h3>

        <label for="username">Email</label>
        <input
          v-model="username"
          type="text"
          placeholder="Email"
          id="username"
        />
        <div v-if="validation == false" class="validate">Invalid email!</div>


        <label for="password">Password</label>
        <input
          v-model="password"
          type="password"
          placeholder="Password"
          id="password"
        />

        <button type="submit" @click="onSubmit1">Log In</button>
        <div class="social">
          <div class="go">Google</div>
          <div class="fb">Facebook</div>
        </div>
      </form>
    </div>
  </div>
</template>

